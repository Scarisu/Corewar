#include <vm.h>

void	put_hexa(char *map, int pc, int value)
{
	int		i;
	char	tmp[4];

	i = -1;
	while (++i < 4)
	{
		tmp[i] = (char)value;
		value <<= 8;
	}
	ft_memcpy(&map[pc + 1], tmp, 4);
}
